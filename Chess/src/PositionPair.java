
public class PositionPair {
	private int v;
	private int h;
	
	public PositionPair (int v , int h) {
		this.v = v;
		this.h = h;
	}
	
	public PositionPair () {
		
	}
	
	public int getVPos () {
		return v;
	}
	
	public void setVPos (int v) {
		this.v = v;
	}
	
	public int getHPos () {
		return h;
	}
	
	public void setHPos (int h) {
		this.h = h;
	}
	
	
	
}//end of class
